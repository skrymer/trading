-- US exchange holidays sourced from EODHD's /exchange-details/US endpoint
-- (snapshot taken 2026-04-30, covering 1995-01-01 through 2030-12-25).
-- Includes substitute days (Christmas/Independence Day on weekends) and
-- one extra (`National Mourning Day 2018`) — kept as 'official' Type.
--
-- Holidays through 2030 ship as static data; revisit (re-fetch + extend)
-- before 2030 to cover later years. Rate of change is low — exchanges
-- announce a year ahead at most — so static is appropriate.
--
-- IngestionService queries this table to drop phantom provider bars
-- stamped to closed market days. Existing phantom rows in `quotes` are
-- cleaned up by re-ingesting affected symbols via the data-management
-- UI — re-ingestion pulls fresh bars through the new holiday filter and
-- replaces the symbol's full series.
CREATE TABLE market_holidays (
    exchange     VARCHAR(10) NOT NULL,
    quote_date   DATE        NOT NULL,
    holiday_name VARCHAR(200),
    holiday_type VARCHAR(20),
    updated_at   TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (exchange, quote_date)
);

CREATE INDEX idx_market_holidays_quote_date ON market_holidays (quote_date);

